const regex = {
    name: /^(?<fname>[a-z]{2,}) (?<lname>[a-z]{2,})$/i,
    mail: /^(?!.*\.\.)([a-z0-9][\w\d\+\.\-]{0,62}[a-z0-9])@(([a-z0-9][a-z0-9\-]{2,62}[a-z0-9])(\.[a-z0-9]{2,3}){1,2})$/i,
    line: /^(\+\d{1,3})\s(\d{1,3}\s?\d{3,5}\s?\d{3,4}\s?\d{3,5}|\d{2,4}\s?\d{3,5}\s?\d{3,6})$/,
}

export default regex;